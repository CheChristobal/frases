    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //inicializamos la variable de la tabla frases
        int seleccion = tablaFrases.getSelectedRow();
        
        //Creamos un string valor para buscar en la base de datos
        String valor= tablaFrases.getValueAt(seleccion, 0).toString();
        //hacemos un if para la selecion de la tabla
        if(seleccion>0){
            //hacemos un try catch para borrar los datos selecionados
            try {
                //hacemos la query para borrar los datos selecionados
                PreparedStatement pst = cn.prepareStatement("DELETE FROM frases WHERE autor='"+valor+"'");
                pst.executeUpdate();
                //mostramos que se borro el dato
                JOptionPane.showMessageDialog(null, "Dato eliminado");
                mostraDatos();
            } catch (SQLException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

