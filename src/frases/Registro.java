    void mostraDatos(){
        //creamos modelo de la tabla
        DefaultTableModel modelo= new DefaultTableModel();
        TableRowSorter trs;
        //agregamos los nombres de las tablas 
        modelo.addColumn("Autor");
        modelo.addColumn("Frase");
        //unimos la tabla al modelo
        tablaFrases.setModel(modelo);
        
        
        //hacemos un string datos con dos valores
        String[] datos = new String [2];
        //hacemos el try catch
        try {
            //hacemos la query
            Statement st= cn.createStatement();
            ResultSet rs= st.executeQuery("SELECT * FROM frases");
            //hacemos el bucle while de la query
            while(rs.next()){
                //agregamos los valores de la query
                datos[0]= rs.getString(1);
                datos[1]= rs.getString(2);
                modelo.addRow(datos);
            }
            //setear el modelo de la tabla
            tablaFrases.setModel(modelo);
        } catch (Exception e) {
        }
    }
    
    

